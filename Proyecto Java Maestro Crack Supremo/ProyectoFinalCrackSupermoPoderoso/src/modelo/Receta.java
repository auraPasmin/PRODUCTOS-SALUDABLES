package modelo;

public class Receta {
    private String p, n;
    private int cantidad;

    public String getP() {
        return p;
    }

    public String getN() {
        return n;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setP(String p) {
        this.p = p;
    }

    public void setN(String n) {
        this.n = n;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }
    
}
